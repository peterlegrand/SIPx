            var ProcessTemplateCreateGetWithErrorMessage = await _client.PostProtectedAsync<ProcessTemplateCreateGetWithErrorMessages>($"{_configuration["APIUrl"]}api/ProcessTemplate/Create", ProcessTemplate, token);
