            var token = HttpContext.Session.GetString("Token");if(token == null){ return RedirectToAction("Login","FrontAuth");}
            var ProjectTypeCreateGetWithErrorMessage = await _client.PostProtectedAsync<ProjectTypeCreateGetWithErrorMessages>($"{_configuration["APIUrl"]}api/ProjectType/Create", ProjectType, token);
            if (ProjectTypeCreateGetWithErrorMessage.ErrorMessages.Count > 0)
