CREATE PROCEDURE [dbo].[usp_ProcessTemplateFieldLanguageUpdate] (
	@ProcessTemplateFieldLanguageId int
	, @Name nvarchar(50)
	, @Description nvarchar(max)
	, @MenuName nvarchar(50)
	, @MouseOver nvarchar(50)
	, @UserId nvarchar(450)) 
AS 
UPDATE  ProcessTemplateFieldLanguages SET 
	Name = @Name
	, Description = @Description
	, MenuName = @MenuName
	, MouseOver = @MouseOver
	, ModifierId = @UserID
	, ModifiedDate = getdate()
WHERE ProcessTemplateFieldLanguageID= @ProcessTemplateFieldLanguageID
